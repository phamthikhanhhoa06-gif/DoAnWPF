-- 1. Bảng LOAIPHONG (Bảng 10)
CREATE TABLE LOAIPHONG (
    Ma_LP INT PRIMARY KEY,
    Ten_TP NVARCHAR(100),
    DonGia_LP BIGINT
);

-- 2. Bảng PHONG (Bảng 9)
CREATE TABLE PHONG (
    Ma_Phong INT PRIMARY KEY,
    TinhTrang_Phong NVARCHAR(50),
    Ma_LP INT,
    CONSTRAINT FK_Phong_LoaiPhong FOREIGN KEY (Ma_LP) REFERENCES LOAIPHONG(Ma_LP)
);

-- 3. Bảng TAIKHOAN (Bảng 1)
CREATE TABLE TAIKHOAN (
    Ma_TK INT PRIMARY KEY,
    TenDangNhap_TK NVARCHAR(100) NOT NULL,
    MatKhau_TK NVARCHAR(100) NOT NULL
);

-- 4. Bảng NHANVIEN (Bảng 2)
CREATE TABLE NHANVIEN (
    MA_NV INT PRIMARY KEY,
    HoTen_NV NVARCHAR(255) NOT NULL,
    GioiTinh_NV BIT, -- Bool trong C# tương ứng BIT trong SQL
    NgaySinh_NV DATETIME,
    SoDienThoai_NV NVARCHAR(20),
    ChucVu_NV NVARCHAR(100),
    DiaChi_NV NVARCHAR(255),
    NgayVaoLam_NV DATETIME,
    Ma_TK INT,
    CONSTRAINT FK_NhanVien_TaiKhoan FOREIGN KEY (Ma_TK) REFERENCES TAIKHOAN(Ma_TK)
);

-- 5. Bảng KHACHHANG (Bảng 3)
CREATE TABLE KHACHHANG (
    MA_KH INT PRIMARY KEY,
    HoTen_KH NVARCHAR(255) NOT NULL,
    SoDienThoai_KH NVARCHAR(20),
    CMND_KH NVARCHAR(20),
    Ma_TK INT,
    CONSTRAINT FK_KhachHang_TaiKhoan FOREIGN KEY(Ma_TK) references TAIKHOAN(Ma_TK)
);

-- 6. Bảng MATHANG (Bảng 11)
CREATE TABLE MATHANG (
    Ma_MH INT PRIMARY KEY,
    Ten_MH NVARCHAR(255),
    DonGia_MH BIGINT,
    NgayNhap_MH DATETIME
);

-- 7. Bảng CHUYENDI (Bảng 12)
CREATE TABLE CHUYENDI (
    Ma_CD INT PRIMARY KEY,
    DiemDen_CD NVARCHAR(255),
    DonGia_CD BIGINT
);
-- 14. Bảng LOAIGIATUI (Bảng 14)
-- Lưu ý: Trong ảnh ghi tên tắt là Ma_MH, nhưng để tránh trùng với bảng MATHANG, 
-- tôi đổi thành Ma_LoaiGU cho logic và dễ quản lý.
CREATE TABLE LOAIGIATUI (
    Ma_LoaiGU INT PRIMARY KEY,
    Ten_LoaiGU NVARCHAR(255),
    DonGia_LoaiGU MONEY
);

-- 8. Bảng LUOTGIATUI (Bảng 13)
CREATE TABLE LUOTGIATUI (
    Ma_LuotGU INT PRIMARY KEY,
    SoKilogram_LuotGU INT,
    NgayBatDau_LuotGU DATETIME,
    NgayKetThuc_LuotGU DATETIME,
	Ma_LoaiGU INT,
	CONSTRAINT FK_LUOTGIATUI_LOAIGIATUI FOREIGN key(Ma_LoaiGU) REFERENCES LOAIGIATUI(Ma_LoaiGU)

);

-- 9. Bảng HOADON (Bảng 4)
CREATE TABLE HOADON (
    MA_HD INT PRIMARY KEY,
    ThoiGianLap_HD DATETIME,
    TinhTrang_HD NVARCHAR(100),
    TriGia_HD BIGINT,
    MA_NV INT, -- Liên kết nhân viên lập hóa đơn
    MA_KH INT, -- Liên kết khách hàng thanh toán
    CONSTRAINT FK_HoaDon_NhanVien FOREIGN KEY (MA_NV) REFERENCES NHANVIEN(MA_NV),
    CONSTRAINT FK_HoaDon_KhachHang FOREIGN KEY (MA_KH) REFERENCES KHACHHANG(MA_KH)
);

-- 10. Bảng CHITIET_HDLT - Chi tiết hóa đơn lưu trú (Bảng 5)
CREATE TABLE CHITIET_HDLT (
    Ma_CTHDLT INT PRIMARY KEY,
    ThoiGianNhan_PHONG DATETIME,
    ThoiGianTra_PHONG DATETIME,
    TriGia_CTHDLT BIGINT,
    MA_HD INT,
    Ma_Phong INT,
    CONSTRAINT FK_CTHDLT_HoaDon FOREIGN KEY (MA_HD) REFERENCES HOADON(MA_HD),
    CONSTRAINT FK_CTHDLT_Phong FOREIGN KEY (Ma_Phong) REFERENCES PHONG(Ma_Phong)
);

-- 11. Bảng CHITIET_HDAU - Chi tiết hóa đơn ăn uống (Bảng 6)
CREATE TABLE CHITIET_HDAU (
    Ma_CTHDAU INT PRIMARY KEY,
    SoLuong_MH INT,
    ThoiGianLap_CTHDAU DATETIME,
    TriGia_CTHDAU BIGINT,
    MA_HD INT,
    Ma_MH INT,
    CONSTRAINT FK_CTHDAU_HoaDon FOREIGN KEY (MA_HD) REFERENCES HOADON(MA_HD),
    CONSTRAINT FK_CTHDAU_MatHang FOREIGN KEY (Ma_MH) REFERENCES MATHANG(Ma_MH)
);

-- 12. Bảng CHITIET_HDDC - Chi tiết hóa đơn di chuyển (Bảng 7)
CREATE TABLE CHITIET_HDDC (
    Ma_CTHDDC INT PRIMARY KEY,
    ThoiGianLap_CTHDDC DATETIME,
    TriGia_CTHDDC BIGINT,
    MA_HD INT,
    Ma_CD INT,
    CONSTRAINT FK_CTHDDC_HoaDon FOREIGN KEY (MA_HD) REFERENCES HOADON(MA_HD),
    CONSTRAINT FK_CTHDDC_ChuyenDi FOREIGN KEY (Ma_CD) REFERENCES CHUYENDI(Ma_CD)
);

-- 13. Bảng CHITIET_HDGU - Chi tiết hóa đơn giặt ủi (Bảng 8)
CREATE TABLE CHITIET_HDGU (
    Ma_CTHDGU INT PRIMARY KEY, -- Lưu ý: Trong ảnh của bạn ghi Ma_CTHDAU (có thể là lỗi đánh máy), tôi đã sửa thành Ma_CTHDGU cho đúng logic.
    ThoiGianLap_CTHDGU DATETIME,
    TriGia_CTHDGU BIGINT,
    MA_HD INT,
    Ma_LuotGU INT,
    CONSTRAINT FK_CTHDGU_HoaDon FOREIGN KEY (MA_HD) REFERENCES HOADON(MA_HD),
    CONSTRAINT FK_CTHDGU_LuotGiat FOREIGN KEY (Ma_LuotGU) REFERENCES LUOTGIATUI(Ma_LuotGU)
);

-- 15. Bảng BAOCAODICHVU (Bảng 15)
CREATE TABLE BAOCAODICHVU (
    MA_BCDV INT PRIMARY KEY,
    THOIGIANLAP_BCDV DATETIME,
    TONGDOANHTHU_BCDV MONEY,
    DOANHTHULUUUTRU_BCDV MONEY,
    DOANHTHUANUONG_BCDV MONEY,
    DOANHTHUGIATUI_BCDV MONEY,
    DOANHTHUDICHUYEN_BCDV MONEY,
    NGAYBATDAU_BCDV DATETIME,
    NGAYKETTHUC_BCDV DATETIME
);

-- 16. Bảng BAOCAONAM (Bảng 16)
CREATE TABLE BAOCAONAM (
    MA_BCN INT PRIMARY KEY,
    THOIGIANLAP_BCN DATETIME,
    NAM_BCN INT,
    TONGDOANHTHU_BCN MONEY,
    DOANHTHUTHANG1_BCN MONEY,
    DOANHTHUTHANG2_BCN MONEY,
    DOANHTHUTHANG3_BCN MONEY,
    DOANHTHUTHANG4_BCN MONEY,
    DOANHTHUTHANG5_BCN MONEY,
    DOANHTHUTHANG6_BCN MONEY,
    DOANHTHUTHANG7_BCN MONEY,
    DOANHTHUTHANG8_BCN MONEY,
    DOANHTHUTHANG9_BCN MONEY,
    DOANHTHUTHANG10_BCN MONEY,
    DOANHTHUTHANG11_BCN MONEY,
    DOANHTHUTHANG12_BCN MONEY
);

